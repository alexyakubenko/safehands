export type ServiceKey = keyof typeof prices;

export function getPriceSelection(service?: string, car?: string) {
  const serviceKey = service && service in prices ? (service as ServiceKey) : "tires";
  const servicePrices = prices[serviceKey];

  if ("table" in servicePrices) {
    return {
      serviceKey,
      service: servicePrices,
      carKey: null,
      car: null,
      table: servicePrices.table,
    };
  }

  const carKey =
    car && car in servicePrices.cars
      ? (car as keyof typeof servicePrices.cars)
      : (Object.keys(servicePrices.cars)[0] as keyof typeof servicePrices.cars);

  return {
    serviceKey,
    carKey,
    service: servicePrices,
    car: servicePrices.cars[carKey],
    table: servicePrices.cars[carKey].table,
  };
}
